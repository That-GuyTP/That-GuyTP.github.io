package edu.mtc.egr281.chapter04;

public class SimpleForChar {

	public static void main(String[] args) {
		
		for(char c = 'A'; c<= 'Z'; ++c) {
			System.out.print(c);
			
		}//EOC for
		System.out.println();
		
		for(char c = 'z'; c >= 'a'; --c) {
			System.out.print(c);
		}//EOC for 2
		
	}//EOC main
	
}//EOC SFC
