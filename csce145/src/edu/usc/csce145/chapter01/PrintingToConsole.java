package edu.usc.csce145.chapter01;

public class PrintingToConsole {

	public static void main(String[] args) {
		System.out.println("Hello World!");
		//println is used as a base statement to print out words.
		System.out.print("Java is fun");
		//print can be used to continue on the same line rather than making a new one.
		System.out.println("\t\"Java\" is fun!!!");
		//use \"x\" for getting around "" limitations
		// \t can be used to add 4 spaces to the start of the print.
		
		//Use /* for multiline psuedocode
		//Ex:
		/* Note header
		 * Note body (continue with enter to add more lines automatically)
		 */
		
		//Further escape characters
		// \" double quote, \' Single quote, 
		
		
		System.exit(0);
	}//EOC main
	
}//EOC class
